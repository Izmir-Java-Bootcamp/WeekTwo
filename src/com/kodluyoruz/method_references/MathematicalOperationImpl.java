package com.kodluyoruz.method_references;

public class MathematicalOperationImpl {

    public static int add(int num1, int num2) {
        return num1 + num2;
    }

    public static int multiply(int num1, int num2) {
        return num1 * num2;
    }

    public static double multiply(int num1, double num2) {
        return num1 * num2;
    }

    public static int extract(int num1, int num2) {
        return num1 - num2;
    }
}
